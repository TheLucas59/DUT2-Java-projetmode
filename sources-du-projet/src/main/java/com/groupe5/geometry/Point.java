package com.groupe5.geometry;

public class Point {
	private double x;
	private double y;
	private double z;
	int id;
	
	public Point(double x, double y, double z, int id) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getZ() {
		return z;
	}
	
	
}
